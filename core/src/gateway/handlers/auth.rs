//! Authentication Handlers
//!
//! Handles device authentication, pairing, and connection management.

use serde::{Deserialize, Serialize};
use serde_json::{json, Value};
use crate::sync_primitives::Arc;
use tracing::{debug, info, warn};

use crate::gateway::device_store::{ApprovedDevice, DeviceStore};
use crate::gateway::protocol::{JsonRpcRequest, JsonRpcResponse, AUTH_FAILED, INVALID_PARAMS};
use crate::gateway::handlers::parse_params;
use crate::gateway::security::{DeviceRole, DeviceType, PairingManager, PairingRequest, TokenManager};
use crate::gateway::security::store::DeviceUpsertData;
use crate::gateway::security::SecurityStore;

/// Parameters for the "hello" method (server -> client notification)
#[derive(Debug, Clone, Serialize)]
pub struct HelloParams {
    /// Protocol version
    pub version: String,
    /// Server name
    pub server: String,
    /// Whether authentication is required
    pub auth_required: bool,
}

/// Parameters for the "connect" method
#[derive(Debug, Clone, Deserialize)]
pub struct ConnectParams {
    /// Device token (for returning devices)
    pub token: Option<String>,
    /// Guest invitation token (for guest access)
    pub invitation_token: Option<String>,
    /// Device name (for new devices)
    pub device_name: Option<String>,
    /// Device type (macos, ios, android, cli, web)
    pub device_type: Option<String>,
    /// Unique device identifier (generated by client)
    pub device_id: Option<String>,
}

/// Result of a successful connection
#[derive(Debug, Clone, Serialize)]
pub struct ConnectResult {
    /// Session token for this connection
    pub token: String,
    /// Device ID
    pub device_id: String,
    /// Permissions granted
    pub permissions: Vec<String>,
    /// Token expiry time (ISO 8601)
    pub expires_at: String,
}

/// Pairing required notification
#[derive(Debug, Clone, Serialize)]
pub struct PairingRequiredParams {
    /// 8-character Base32 pairing code
    pub code: String,
    /// Seconds until code expires
    pub expires_in: u64,
    /// Instructions for user
    pub message: String,
}

/// Authentication context for handlers
pub struct AuthContext {
    pub token_manager: Arc<TokenManager>,
    pub pairing_manager: Arc<PairingManager>,
    pub device_store: Arc<DeviceStore>,
    pub security_store: Arc<SecurityStore>,
    pub invitation_manager: Arc<crate::gateway::security::InvitationManager>,
    pub guest_session_manager: Arc<crate::gateway::security::GuestSessionManager>,
    pub event_bus: Arc<crate::gateway::event_bus::GatewayEventBus>,
    pub require_auth: bool,
}

// AuthContext fields are all `pub`, so it is constructed directly via struct literal.

/// Handle "connect" request
///
/// This is the main authentication endpoint. Clients must call this
/// before any other methods when auth is required.
pub async fn handle_connect(
    request: JsonRpcRequest,
    ctx: Arc<AuthContext>,
) -> JsonRpcResponse {
    // Parse parameters
    let params: ConnectParams = match &request.params {
        Some(Value::Object(map)) => match serde_json::from_value(Value::Object(map.clone())) {
            Ok(p) => p,
            Err(e) => {
                return JsonRpcResponse::error(
                    request.id,
                    INVALID_PARAMS,
                    format!("Invalid connect params: {}", e),
                );
            }
        },
        _ => ConnectParams {
            token: None,
            invitation_token: None,
            device_name: None,
            device_type: None,
            device_id: None,
        },
    };

    // Check for guest invitation token FIRST (before require_auth bypass)
    // Guest invitations should work regardless of require_auth setting
    if let Some(invitation_token) = &params.invitation_token {
        debug!("Processing guest invitation token: {}", invitation_token);
        match ctx.invitation_manager.activate_invitation(invitation_token) {
            Ok(guest_token) => {
                debug!("Guest invitation activated successfully for guest_id: {}", guest_token.guest_id);

                // Generate a unique session ID
                let session_id = uuid::Uuid::new_v4().to_string();
                let connection_id = "pending".to_string(); // Will be updated by server

                // Get guest name from scope or use default
                let guest_name = guest_token
                    .scope
                    .display_name
                    .clone()
                    .unwrap_or_else(|| "Guest".to_string());

                // Register guest session
                let session = ctx.guest_session_manager.register_session(
                    session_id.clone(),
                    guest_token.guest_id.clone(),
                    guest_name.clone(),
                    connection_id,
                    guest_token.scope.clone(),
                );

                // Emit session connected event
                let event = crate::gateway::event_bus::TopicEvent {
                    topic: "guest.session.connected".to_string(),
                    data: serde_json::json!({
                        "session_id": session.session_id,
                        "guest_id": session.guest_id,
                        "guest_name": session.guest_name,
                        "connected_at": session.connected_at
                    }),
                    timestamp: std::time::SystemTime::now()
                        .duration_since(std::time::UNIX_EPOCH)
                        .unwrap()
                        .as_millis() as u64,
                };
                let _ = ctx.event_bus.publish_json(&event);

                info!(
                    guest_id = %guest_token.guest_id,
                    session_id = %session_id,
                    "Guest session created via invitation"
                );

                // Return session info (no persistent token for guests)
                return JsonRpcResponse::success(
                    request.id,
                    json!(ConnectResult {
                        token: format!("guest:{}:{}", session_id, guest_token.token),
                        device_id: format!("guest-{}", guest_token.guest_id),
                        permissions: guest_token
                            .scope
                            .allowed_tools
                            .iter()
                            .map(|t| format!("tool:{}", t))
                            .collect(),
                        expires_at: guest_token
                            .scope
                            .expires_at
                            .and_then(chrono::DateTime::from_timestamp_millis)
                            .map(|dt| dt.to_rfc3339())
                            .unwrap_or_else(|| {
                                (chrono::Utc::now() + chrono::Duration::hours(24)).to_rfc3339()
                            }),
                    }),
                );
            }
            Err(e) => {
                debug!(error = %e, "Invalid invitation token");
                return JsonRpcResponse::error(
                    request.id,
                    -32001,
                    format!("Invalid invitation: {}", e),
                );
            }
        }
    }

    // If authentication is not required, allow any connection
    if !ctx.require_auth {
        let device_id = params
            .device_id
            .unwrap_or_else(|| uuid::Uuid::new_v4().to_string());

        // Register device in SecurityStore first (required for FK constraint on tokens)
        let device_name = params.device_name.as_deref().unwrap_or("Auto-Device");
        if let Err(e) = ctx.security_store.upsert_device(&DeviceUpsertData {
            device_id: &device_id,
            device_name,
            device_type: None,
            public_key: &[0u8; 32], // Placeholder public key
            fingerprint: &device_id[..16.min(device_id.len())], // Use prefix as fingerprint
            role: DeviceRole::Operator.as_str(),
            scopes: &["*".to_string()],
        }) {
            warn!(error = %e, "Failed to register device");
            return JsonRpcResponse::error(request.id, -32603, format!("Failed to register device: {}", e));
        }

        let signed_token = match ctx
            .token_manager
            .issue_token(&device_id, DeviceRole::Operator, vec!["*".to_string()])
        {
            Ok(t) => t,
            Err(e) => {
                warn!(error = %e, "Failed to issue token");
                return JsonRpcResponse::error(request.id, -32603, format!("Failed to issue token: {}", e));
            }
        };

        info!(device_id = %device_id, "Connection accepted (auth not required)");

        return JsonRpcResponse::success(
            request.id,
            json!(ConnectResult {
                token: format!("{}:{}", signed_token.token, signed_token.signature),
                device_id,
                permissions: vec!["*".to_string()],
                expires_at: chrono::DateTime::from_timestamp_millis(signed_token.expires_at)
                    .map(|dt| dt.to_rfc3339())
                    .unwrap_or_else(|| (chrono::Utc::now() + chrono::Duration::hours(24)).to_rfc3339()),
            }),
        );
    }

    debug!("Processing connect request: device_id={:?}, has_token={}, has_invitation_token={}",
        params.device_id, params.token.is_some(), params.invitation_token.is_some());

    // Case 1: Client has a token - validate it
    if let Some(token_str) = &params.token {
        debug!("Case 1: Validating existing token");
        // Token format: "{token}:{signature}"
        if let Some((token, signature)) = token_str.split_once(':') {
            match ctx.token_manager.validate_token(token, signature) {
                Ok(validation) => {
                    // Security: If client provides a device_id, it must match the token's
                    // device_id to prevent identity spoofing (using a valid token from
                    // device-A to impersonate device-B).
                    if let Some(ref claimed_id) = params.device_id {
                        if claimed_id != &validation.device_id {
                            return JsonRpcResponse::error(
                                request.id,
                                -32001,
                                "device_id mismatch: token was issued for a different device",
                            );
                        }
                    }
                    let device_id = validation.device_id.clone();

                    // Update last seen time if device is in store
                    let _ = ctx.device_store.update_last_seen(&device_id);

                    info!(device_id = %device_id, "Connection authenticated via token");

                    return JsonRpcResponse::success(
                        request.id,
                        json!(ConnectResult {
                            token: token_str.clone(),
                            device_id,
                            permissions: validation.scopes,
                            expires_at: chrono::DateTime::from_timestamp_millis(
                                chrono::Utc::now().timestamp_millis() + validation.remaining_ms
                            )
                            .map(|dt| dt.to_rfc3339())
                            .unwrap_or_else(|| (chrono::Utc::now() + chrono::Duration::hours(24)).to_rfc3339()),
                        }),
                    );
                }
                Err(e) => {
                    debug!(error = %e, "Invalid token provided");
                    // Token invalid, fall through to pairing
                }
            }
        } else {
            debug!("Invalid token format (expected token:signature)");
            // Token invalid, fall through to pairing
        }
    }

    // Case 2: Check if device_id is already approved
    if let Some(device_id) = &params.device_id {
        debug!("Case 2: Checking if device is approved: {}", device_id);
        if ctx.device_store.is_approved(device_id) {
            debug!("Device is approved, issuing token");
            // Device is approved, generate new token
            let device = ctx.device_store.get_device(device_id);
            let permissions = device
                .as_ref()
                .map(|d| d.permissions.clone())
                .unwrap_or_else(|| vec!["*".to_string()]);

            let signed_token = match ctx
                .token_manager
                .issue_token(device_id, DeviceRole::Operator, permissions.clone())
            {
                Ok(t) => t,
                Err(e) => {
                    warn!(error = %e, "Failed to issue token");
                    return JsonRpcResponse::error(request.id, -32603, format!("Failed to issue token: {}", e));
                }
            };

            // Update last seen
            let _ = ctx.device_store.update_last_seen(device_id);

            info!(device_id = %device_id, "Connection authenticated via approved device");

            return JsonRpcResponse::success(
                request.id,
                json!(ConnectResult {
                    token: format!("{}:{}", signed_token.token, signed_token.signature),
                    device_id: device_id.clone(),
                    permissions,
                    expires_at: chrono::DateTime::from_timestamp_millis(signed_token.expires_at)
                        .map(|dt| dt.to_rfc3339())
                        .unwrap_or_else(|| (chrono::Utc::now() + chrono::Duration::hours(24)).to_rfc3339()),
                }),
            );
        }
    }

    // Case 3: New device - initiate pairing
    debug!("Case 3: Initiating new device pairing");
    let device_name = params
        .device_name
        .unwrap_or_else(|| "Unknown Device".to_string());
    let _device_type = params.device_type;
    let _device_id = params
        .device_id
        .unwrap_or_else(|| uuid::Uuid::new_v4().to_string());

    // Check if there's already a pending pairing for this device
    // (to prevent spamming pairing requests)
    let pending = match ctx.pairing_manager.list_pending() {
        Ok(p) => p,
        Err(e) => {
            warn!(error = %e, "Failed to list pending pairings");
            return JsonRpcResponse::error(request.id, -32603, format!("Failed to list pending pairings: {}", e));
        }
    };

    // Find existing device pairing by name
    if let Some(existing) = pending.iter().find(|req| {
        matches!(req, PairingRequest::Device { device_name: name, .. } if name == &device_name)
    }) {
        let code = existing.code();
        let remaining = existing.remaining_secs();
        info!(
            device_name = %device_name,
            code = %code,
            "Returning existing pairing code"
        );

        return JsonRpcResponse::error_with_data(
            request.id,
            AUTH_FAILED,
            "pairing_required",
            json!(PairingRequiredParams {
                code: code.to_string(),
                expires_in: remaining,
                message: format!(
                    "Enter code {} to approve this device, or run: aleph-gateway pairing approve {}",
                    code, code
                ),
            }),
        );
    }

    // Initiate new pairing (device pairing without public key for now - legacy compatibility)
    // In a full Ed25519 implementation, the client would provide a public key
    let pairing_request = match ctx.pairing_manager.request_device_pairing(
        device_name.clone(),
        None, // device_type parsed as DeviceType
        vec![0u8; 32], // placeholder public key for legacy API
        None, // remote_addr
    ) {
        Ok(req) => req,
        Err(e) => {
            warn!(error = %e, "Failed to initiate pairing");
            return JsonRpcResponse::error(request.id, -32603, format!("Failed to initiate pairing: {}", e));
        }
    };

    let code = pairing_request.code().to_string();
    let expires_in = pairing_request.remaining_secs();

    info!(
        device_name = %device_name,
        code = %code,
        "Pairing initiated"
    );

    JsonRpcResponse::error_with_data(
        request.id,
        AUTH_FAILED,
        "pairing_required",
        json!(PairingRequiredParams {
            code: code.clone(),
            expires_in,
            message: format!(
                "Enter code {} to approve this device, or run: aleph-gateway pairing approve {}",
                code, code
            ),
        }),
    )
}

/// Handle "pairing.approve" request (from authorized client or CLI)
pub async fn handle_pairing_approve(
    request: JsonRpcRequest,
    ctx: Arc<AuthContext>,
) -> JsonRpcResponse {
    #[derive(Debug, Deserialize)]
    struct ApproveParams {
        code: String,
    }

    let params: ApproveParams = match parse_params(&request) {
        Ok(p) => p,
        Err(e) => return e,
    };

    // Confirm pairing (removes from pending and returns the request)
    let pairing_request = match ctx.pairing_manager.confirm_pairing(&params.code) {
        Ok(req) => req,
        Err(e) => {
            warn!(code = %params.code, error = %e, "Invalid or expired pairing code");
            return JsonRpcResponse::error(request.id, AUTH_FAILED, "Invalid or expired pairing code");
        }
    };

    // Extract device info from pairing request
    let (device_name, device_type): (String, Option<String>) = match &pairing_request {
        PairingRequest::Device { device_name, device_type, .. } => {
            (device_name.clone(), device_type.map(|t: DeviceType| t.as_str().to_string()))
        }
        PairingRequest::Channel { channel, sender_id, .. } => {
            // Channel pairing approved - approve the sender via SecurityStore
            if let Err(e) = ctx.security_store.approve_sender(channel, sender_id) {
                warn!(error = %e, "Failed to approve sender");
                return JsonRpcResponse::error(request.id, -32603, format!("Failed to approve sender: {}", e));
            }
            info!(channel = %channel, sender_id = %sender_id, "Channel sender approved");
            return JsonRpcResponse::success(request.id, json!({
                "channel": channel,
                "sender_id": sender_id,
                "approved": true,
            }));
        }
    };

    // Generate device ID and create approved device
    let device_id = uuid::Uuid::new_v4().to_string();
    let device = ApprovedDevice::new(
        device_id.clone(),
        device_name.clone(),
        device_type,
    );

    // Store in device store
    if let Err(e) = ctx.device_store.approve_device(&device) {
        warn!(error = %e, "Failed to store approved device");
        return JsonRpcResponse::error(
            request.id,
            -32603,
            format!("Failed to store device: {}", e),
        );
    }

    // Register device in SecurityStore for token FK constraint
    if let Err(e) = ctx.security_store.upsert_device(&DeviceUpsertData {
        device_id: &device_id,
        device_name: &device_name,
        device_type: None,
        public_key: &[0u8; 32], // placeholder public key
        fingerprint: &device_id[..16.min(device_id.len())], // use device_id prefix as fingerprint
        role: "operator",
        scopes: &["*".to_string()],
    }) {
        warn!(error = %e, "Failed to register device in security store");
        return JsonRpcResponse::error(
            request.id,
            -32603,
            format!("Failed to register device: {}", e),
        );
    }

    // Generate token for the new device
    let signed_token = match ctx
        .token_manager
        .issue_token(&device_id, DeviceRole::Operator, vec!["*".to_string()])
    {
        Ok(t) => t,
        Err(e) => {
            warn!(error = %e, "Failed to issue token");
            return JsonRpcResponse::error(request.id, -32603, format!("Failed to issue token: {}", e));
        }
    };

    info!(
        device_id = %device_id,
        device_name = %device_name,
        "Device pairing approved"
    );

    JsonRpcResponse::success(
        request.id,
        json!({
            "device_id": device_id,
            "device_name": device_name,
            "token": format!("{}:{}", signed_token.token, signed_token.signature),
            "approved_at": device.approved_at,
        }),
    )
}

/// Handle "pairing.reject" request
pub async fn handle_pairing_reject(
    request: JsonRpcRequest,
    ctx: Arc<AuthContext>,
) -> JsonRpcResponse {
    #[derive(Debug, Deserialize)]
    struct RejectParams {
        code: String,
    }

    let params: RejectParams = match parse_params(&request) {
        Ok(p) => p,
        Err(e) => return e,
    };

    match ctx.pairing_manager.cancel_pairing(&params.code) {
        Ok(true) => {
            info!(code = %params.code, "Pairing rejected");
            JsonRpcResponse::success(request.id, json!({"rejected": true}))
        }
        Ok(false) => {
            JsonRpcResponse::error(request.id, AUTH_FAILED, "Invalid or expired pairing code")
        }
        Err(e) => {
            warn!(error = %e, "Failed to cancel pairing");
            JsonRpcResponse::error(request.id, -32603, format!("Failed to cancel pairing: {}", e))
        }
    }
}

/// Handle "pairing.list" request
pub async fn handle_pairing_list(
    request: JsonRpcRequest,
    ctx: Arc<AuthContext>,
) -> JsonRpcResponse {
    let pending = match ctx.pairing_manager.list_pending() {
        Ok(p) => p,
        Err(e) => {
            warn!(error = %e, "Failed to list pending pairings");
            return JsonRpcResponse::error(request.id, -32603, format!("Failed to list pending pairings: {}", e));
        }
    };

    let items: Vec<Value> = pending
        .into_iter()
        .map(|req| {
            match req {
                PairingRequest::Device { code, device_name, device_type, expires_at, created_at, .. } => {
                    let remaining = if expires_at > created_at {
                        let now = std::time::SystemTime::now()
                            .duration_since(std::time::UNIX_EPOCH)
                            .unwrap()
                            .as_millis() as i64;
                        if expires_at > now {
                            ((expires_at - now) / 1000) as u64
                        } else {
                            0
                        }
                    } else {
                        0
                    };
                    json!({
                        "type": "device",
                        "code": code,
                        "device_name": device_name,
                        "device_type": device_type.map(|t: DeviceType| t.as_str()),
                        "expires_in": remaining,
                    })
                }
                PairingRequest::Channel { code, channel, sender_id, expires_at, created_at, .. } => {
                    let remaining = if expires_at > created_at {
                        let now = std::time::SystemTime::now()
                            .duration_since(std::time::UNIX_EPOCH)
                            .unwrap()
                            .as_millis() as i64;
                        if expires_at > now {
                            ((expires_at - now) / 1000) as u64
                        } else {
                            0
                        }
                    } else {
                        0
                    };
                    json!({
                        "type": "channel",
                        "code": code,
                        "channel": channel,
                        "sender_id": sender_id,
                        "expires_in": remaining,
                    })
                }
            }
        })
        .collect();

    JsonRpcResponse::success(request.id, json!({"pending": items}))
}

/// Handle "devices.list" request
pub async fn handle_devices_list(
    request: JsonRpcRequest,
    ctx: Arc<AuthContext>,
) -> JsonRpcResponse {
    let devices = ctx.device_store.list_devices();

    let items: Vec<Value> = devices
        .into_iter()
        .map(|d| {
            json!({
                "device_id": d.device_id,
                "device_name": d.device_name,
                "device_type": d.device_type,
                "approved_at": d.approved_at,
                "last_seen_at": d.last_seen_at,
                "permissions": d.permissions,
            })
        })
        .collect();

    JsonRpcResponse::success(request.id, json!({"devices": items}))
}

/// Handle "devices.revoke" request
pub async fn handle_devices_revoke(
    request: JsonRpcRequest,
    ctx: Arc<AuthContext>,
) -> JsonRpcResponse {
    #[derive(Debug, Deserialize)]
    struct RevokeParams {
        device_id: String,
    }

    let params: RevokeParams = match parse_params(&request) {
        Ok(p) => p,
        Err(e) => return e,
    };

    // Revoke device from store
    match ctx.device_store.revoke_device(&params.device_id) {
        Ok(true) => {
            // Also revoke any tokens for this device
            if let Err(e) = ctx.token_manager.revoke_device_tokens(&params.device_id) {
                warn!(error = %e, "Failed to revoke device tokens");
            }

            info!(device_id = %params.device_id, "Device revoked");
            JsonRpcResponse::success(request.id, json!({"revoked": true}))
        }
        Ok(false) => {
            JsonRpcResponse::error(request.id, -32004, "Device not found")
        }
        Err(e) => {
            JsonRpcResponse::error(request.id, -32603, format!("Failed to revoke: {}", e))
        }
    }
}

