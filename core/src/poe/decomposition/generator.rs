#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn generate_simple_creates_correct_count() {
        let parent = SuccessManifest::new("parent-1", "Parent objective");
        let subs = vec![
            "Sub task A".to_string(),
            "Sub task B".to_string(),
            "Sub task C".to_string(),
        ];

        let result = SubManifestGenerator::generate_simple(&parent, &subs);
        assert_eq!(result.len(), 3);
    }

    #[test]
    fn sub_manifest_task_ids_inherit_parent_prefix() {
        let parent = SuccessManifest::new("my-task", "Parent objective");
        let subs = vec!["First".to_string(), "Second".to_string()];

        let result = SubManifestGenerator::generate_simple(&parent, &subs);
        assert_eq!(result[0].task_id, "my-task-sub-0");
        assert_eq!(result[1].task_id, "my-task-sub-1");
    }

    #[test]
    fn generate_simple_preserves_max_attempts() {
        let parent = SuccessManifest::new("p", "Parent").with_max_attempts(10);
        let subs = vec!["Child".to_string()];

        let result = SubManifestGenerator::generate_simple(&parent, &subs);
        assert_eq!(result[0].max_attempts, 10);
    }

    #[test]
    fn empty_sub_objectives_returns_empty() {
        let parent = SuccessManifest::new("p", "Parent");
        let subs: Vec<String> = vec![];

        let result = SubManifestGenerator::generate_simple(&parent, &subs);
        assert!(result.is_empty());
    }

    #[test]
    fn generate_simple_has_no_constraints() {
        let parent = SuccessManifest::new("p", "Parent")
            .with_hard_constraint(crate::poe::types::ValidationRule::FileExists {
                path: std::path::PathBuf::from("test.txt"),
            });

        let subs = vec!["Child task".to_string()];
        let result = SubManifestGenerator::generate_simple(&parent, &subs);

        assert!(result[0].hard_constraints.is_empty());
        assert!(result[0].soft_metrics.is_empty());
    }

    #[test]
    fn sub_manifest_objectives_match_input() {
        let parent = SuccessManifest::new("p", "Parent");
        let subs = vec![
            "Implement authentication".to_string(),
            "Write unit tests".to_string(),
        ];

        let result = SubManifestGenerator::generate_simple(&parent, &subs);
        assert_eq!(result[0].objective, "Implement authentication");
        assert_eq!(result[1].objective, "Write unit tests");
    }
}
